    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) 
    {                                         
        try 
        { // Try get image 
            JFileChooser imageFileChooser = new JFileChooser(new File("."));
            int stateImageFileChooser = imageFileChooser.showOpenDialog(null);

            if (stateImageFileChooser == JFileChooser.APPROVE_OPTION) 
            {
                JFrame frame = new JFrame("");
                frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                String file = imageFileChooser.getSelectedFile().getPath();
                this.noiseImageFile = file;

                ImageProcess process = new ImageProcess(file);
                ImageIcon imageIcon = new ImageIcon(file);
                
                ImageIcon scaledImage = scaleImage(imageIcon.getImage());
                this.jLabel2.setIcon(scaledImage);
                this.noiseImage = scaledImage;
            }
        } 
        catch (Exception E) 
        {
        }
    }
    
