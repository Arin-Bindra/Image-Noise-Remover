    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        try 
        {
            if(this.noiseImageFile != null)
            {
                ImageProcess process2 = new ImageProcess(this.noiseImageFile);
                ImageIcon image2 = new ImageIcon(this.noiseImageFile);

                process2.cleanNoise();
                process2.save("noise_removed.jpg");

                ImageIcon imageIcon = new ImageIcon("noise_removed.jpg");

                ImageIcon largeImage2 = scaleImage(imageIcon.getImage());
                this.jLabel2.setIcon(largeImage2);
                this.cleanImage = largeImage2;
            }
        }
        catch (Exception e) 
        {
        }
    }
    
